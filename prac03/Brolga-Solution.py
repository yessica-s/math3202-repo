# Site ID: Capacity (L), Price ($/L)
W = {
	10: [2300,2.14],
	30: [2600,2.08],
	35: [2500,1.87],
	37: [1800,1.81]
}

# Cost for driving ($/km)
TravelCost = 0.15
k = .02

# Sets
S = range(len(Sites))  # burn sites and warehouses
R = range(len(Roads))

m = gp.Model("Brolga")

# Variables
Y = { s: m.addVar() for s in S }
X = { r: m.addVar() for r in R }

# Objective
m.setObjective(gp.quicksum(W[w][1]*Y[w] for w in W) +
               gp.quicksum(TravelCost*Roads[r][3]*X[r] for r in R))

# Constraints

# Flow balance (including loss in Communication 3)
for s in S:
    m.addConstr(Y[s] + gp.quicksum(X[r]*(1 - k*Roads[r][3]/100) for r in R if Roads[r][2] == s) ==
                Sites[s][3] + gp.quicksum(X[r] for r in R if Roads[r][1] == s))

# Capacity 
for s in S:
    if s in W:
        m.addConstr(Y[s] <= W[s][0])
    else:
        m.addConstr(Y[s] == 0) # not a warehouse node

# Roads (Communication 2)
for r in R:
    m.addConstr(X[r] <= Roads[r][4])        
        
m.optimize()

print(m.objVal)




